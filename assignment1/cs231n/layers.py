    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    N,c = x.shape
    exp_scores = np.exp(x)
    exp_sum = np.sum(exp_scores, axis=1)
    probs = exp_scores / exp_sum.reshape((N,1))
    loss = np.sum(-1*np.log(probs[range(N), y]))/N
    probs[range(N), y] = probs[range(N), y] - 1

    dx = probs/N
